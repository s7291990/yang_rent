// 지역별 순서 변경
export const sortCarsByRegion = (cars: Car[], region: string) => {
  const order = regionOrder[region as keyof typeof regionOrder];
  if (!order) return cars; // fallback
  // order 배열에 없는 car는 뒤로
  return [...cars].sort((a, b) => {
    const ai = order.indexOf(a.id); // 또는 a.code, a.name 등
    const bi = order.indexOf(b.id);
    if (ai === -1 && bi === -1) return 0;
    if (ai === -1) return 1;
    if (bi === -1) return -1;
    return ai - bi;
  });
};
